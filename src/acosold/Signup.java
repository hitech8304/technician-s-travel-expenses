    private void jButton_EnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EnregistrerActionPerformed
        String fName = jTextField_Nom.getText();
        String lName = jTextField_Prenom.getText();
        String password = String.valueOf(jPasswordField_Password.getPassword());
        String rePassword = String.valueOf(jPasswordField_RePassword.getPassword());
        
        if(!fName.equals("") && !lName.equals("") && !password.equals("") && !rePassword.equals(""))
        {
            if(password.equals(rePassword))
            {
                // execute the methode that will add new user to database from Signin.java file
                signin.addUser(fName, lName, password);
                //empty all fields after signup seccess
                        Login l = new Login();
                        l.pack();
                        l.setVisible(true);
                        l.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                        l.setLocationRelativeTo(null);
                        this.dispose();
                        
            }
            else 
            {
                JOptionPane.showMessageDialog(null, "Les champs de mot de passe sont pas identique!");
                jTextField_Nom.setText("");jTextField_Prenom.setText("");jPasswordField_Password.setText("");
                        jPasswordField_RePassword.setText("");
            }
        }
        else
        {
             JOptionPane.showMessageDialog(null, "Aucun champ vide n'est autoris√©!");
                        jTextField_Nom.setText("");
                        jTextField_Prenom.setText("");
                        jPasswordField_Password.setText("");
                        jPasswordField_RePassword.setText("");
        }
    }//GEN-LAST:event_jButton_EnregistrerActionPerformed

